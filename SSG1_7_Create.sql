CREATE TABLE EMP(
	EMPNO TINYINT PRIMARY KEY,
	ENAME NVARCHAR(10),
	JOB CHAR(9),
	MGR TINYINT,
	HIREDATE DATE,
	SAL DECIMAL(7,2),
	COMM DECIMAL(7,2),
	DEPTNO TINYINT NOT NULL,
	CONSTRAINT Emp_MGR
	FOREIGN KEY(MGR)
	REFERENCES EMP(EMPNO),
	CONSTRAINT Emp_Dept
	FOREIGN KEY(DEPTNO)
	REFERENCES DEPT(DEPTNO)
)
GO